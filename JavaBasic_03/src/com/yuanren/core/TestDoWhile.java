package com.yuanren.core;

public class TestDoWhile {

    public static void main(String[] args) {
       /* 初始化值；
        do {
            代码块;
            步长；
        }while(判断条件);
       */


        int  num=10;
        do {
            System.out.println("我喜欢吃虾...");
            num--;
        }while (num<0);

        


    }
}
